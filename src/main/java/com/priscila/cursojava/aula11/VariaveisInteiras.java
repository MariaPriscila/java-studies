package com.priscila.cursojava.aula11;

public class VariaveisInteiras {
    public static void main(String[] args) {

        byte idade01 = 20; // 1 byte = 8 bits

        short idade02 = 21; // 2 bytes = 16 bits

        Integer idade05 = null; // diferença entre Integer e int é que o Integer é uma classe e o int é um tipo primitivo

        int idade03 = 22; // 4 bytes = 32 bits

        long idade04 = 23; // 8 bytes = 64 bits

        System.out.println("Valor variável idade01 = " + idade01);
        System.out.println("Valor variável idade02 = " + idade02);
        System.out.println("Valor variável idade03 = " + idade03);
        System.out.println("Valor variável idade04 = " + idade04);

    }
}
