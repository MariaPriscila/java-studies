package com.priscila.cursojava.aula11;

public class SequenciaEscape {
        public static void main(String[] args) {

            //"Hello, World!"
            System.out.print("\"Hello, World!\"\n\r"); // \n = new line // \r = carriage return
            // 1\4
            System.out.println("1\\4");
        }
}
